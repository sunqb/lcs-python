# 健康检查程序
体温 = 36.5  # 改成不同的温度试试

print("=== 小小医生程序 ===")
print("你的体温是：", 体温, "度")

if 体温 > 37.5:
    print("你发烧了！要多喝水，好好休息。")
elif 体温 < 36:
    print("体温有点低，要注意保暖哦。")
else:
    print("体温正常，身体很健康！")

# 检查身高
身高 = 120  # 厘米，改成你的身高

